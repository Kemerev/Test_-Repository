# Cписок из 10 элементов
list = [1, "Hello", 3.14, True, "World", 42, False, "Python", 5, 7.5]

# Выводим список
print("Весь список:", list)

# Используя срезы, выводим на экран различные части списка
print("Первые 5 элементов:", list[:5])
print("Последние 3 элемента:", list[-3:])
print("Каждый второй элемент:", list[::2])

# Изменяем 3 элементa
list[2] = "New Value"
print("Измененный список:", list)
