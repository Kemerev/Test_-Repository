def main(x):
    # Указанные промежутки
    e = (-30, -2)  # (-30, -2]
    q = (7, 26)    # (7, 26]
    
    # Проверка принадлежности к промежуткам
    if e[0] < x <= e[1]:
        return "Принадлежит"
    elif q[0] < x <= q[1]:
        return "Принадлежит"
    else:
        return "Не принадлежит"

# Ввод числа
x = int(input("Введите число: "))

# Проверка, вывод
result = main(x)
print(result)
